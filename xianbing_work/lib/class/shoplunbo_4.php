		<h1>《指南》部分内容</h1> 

　　<p>教育部发布《3-6岁儿童学习与发展指南》征求意见稿，严禁“拔苗助长”式的超前教育和强化训练

　　6岁孩子学会10以内加减就可以

　　最近，教育部发布《3-6岁儿童学习与发展指南》(征求意见稿)(以下简称《指南》)，公开征求社会各界意见。

　　《指南》分别对3~4岁、4~5岁、5~6岁三个年龄段末期幼儿应该知道什么、能做什么，大致可以达到什么发展水平提出了合理期望。相关专家表示，这个《指南》是一个科学详细的参考，可以让老师和家长遵循幼儿的发展规律和学习特点，遏制“拔苗助长”式的超前教育和强化训练。

　　不少家长将认字看做衡量孩子是否优秀的标准

　　晨晨今年9月上小学。去年6月，妈妈林女士就决定接下去一年不让晨晨读大班了，而改报了“学前班”。

　　“好几个朋友的小孩都去读过学前班，说进小学适应快，也比没上过的小孩成绩好。”上了半个多学期后，林女士发现，学前班的数学很难，学的是100以内的加减法，很多都是小学里的内容。

　　晨晨外婆很得意，逢人便夸晨晨聪明，把小学生都比下去。但林女士隐隐有些担心：“那么难，担心孩子很累。”</p>
		
		
			
